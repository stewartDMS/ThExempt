/// Layout constants for consistent spacing around the bottom navigation bar.
class LayoutConstants {
  LayoutConstants._();

  /// Height of the bottom navigation bar content area.
  static const double bottomNavHeight = 64.0;

  /// Recommended bottom padding for scrollable content (clears nav + FAB).
  static const double bottomContentPadding = 100.0;

  /// Recommended bottom padding for screens without a FAB.
  static const double bottomNavPadding = 80.0;

  // Standard padding values
  static const double standardPadding = 16.0;
  static const double largePadding = 24.0;
  static const double smallPadding = 8.0;
}
